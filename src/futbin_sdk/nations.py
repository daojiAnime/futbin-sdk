def get_nation_name(nation_id: int) -> str | None:
    """Get nation name by ID

    Args:
        nation_id: The nation ID

    Returns:
        Nation name or None if not found
    """
    return NATIONS.get(nation_id)


def get_nation_id(nation_name: str) -> int | None:
    """Get nation ID by name (case-insensitive)

    Args:
        nation_name: The nation name

    Returns:
        Nation ID or None if not found
    """
    name_lower = nation_name.lower()
    for nid, name in NATIONS.items():
        if name.lower() == name_lower:
            return nid
    return None
